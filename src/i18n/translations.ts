export const getStatusLabel = (language: Language, status: DocumentStatus | 'overdue'): string => {
  const labels = {
    en: { draft: 'Draft', sent: 'Sent', paid: 'Paid', accepted: 'Accepted', declined: 'Declined', overdue: 'Overdue' },
    ar: { draft: 'مسودة', sent: 'مُرسلة', paid: 'مدفوعة', accepted: 'مقبولة', declined: 'مرفوضة', overdue: 'متأخر' },
  }
  return labels[language][status]
}

export const getTypeLabel = (language: Language, type: DocumentType): string =>
  type === 'invoice' ? translations[language].invoice : translations[language].quotation
